//es6-common-js 模块化
// 定义成员：
export function sum(a, b) {
    return parseInt(a) + parseInt(b)
}

export function subtract(a, b) {
    return parseInt(a) - parseInt(b)
}

const multiply = function (a, b) {
    return parseInt(a) * parseInt(b)
}
const divide = function (a, b) {
    return parseInt(a) / parseInt(b)
}

